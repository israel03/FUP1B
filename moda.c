#include <stdio.h>

int calif;
int moda[10];
int i;

int main()
{
	printf("Programa que calcula la moda de 10 calificaciones:\n");
	
	for(i=0;i<10;i++){
		
		
		printf("Escriba la calificacion:\n");
		scanf("%d", &calif);
		moda[i]=0;
		moda[calif["i"]-1]++;
	
	}
	
	printf("Moda: %d\n",calif);
	
	
	
	
	
}
